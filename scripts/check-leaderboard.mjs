import { chromium } from 'playwright';
const BASE = process.env.BASE_URL || 'http://127.0.0.1:4337';
const browser = await chromium.launch();
const page = await browser.newPage();
const errs = [];
page.on('pageerror', e => errs.push(e.message));
await page.goto(`${BASE}/login`, { waitUntil: 'networkidle' });
await page.fill('#email', 'demo@arcistocks.local');
await page.fill('#password', 'demo123456');
await page.click('#email-submit');
await page.waitForURL('**/dashboard', { timeout: 10000 });
await page.goto(`${BASE}/leaderboard`, { waitUntil: 'networkidle' });
await page.waitForTimeout(1000);
const hasToggle = await page.isVisible('button:has-text("30 days")');
await page.click('button:has-text("30 days")');
await page.waitForTimeout(1500);
const rowVisible = await page.isVisible('text=Demo');
console.log('30-day toggle present :', hasToggle);
console.log('row renders after click:', rowVisible);
console.log('page errors           :', errs.length ? errs.map(e=>e.split(';')[0]) : '(none)');
await browser.close();
process.exit(hasToggle && rowVisible && errs.length === 0 ? 0 : 1);
