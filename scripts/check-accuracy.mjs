import { chromium } from 'playwright';
const BASE = process.env.BASE_URL || 'http://127.0.0.1:4340';
const browser = await chromium.launch();
const page = await browser.newPage();
const errs = [];
page.on('pageerror', e => errs.push(e.message));
await page.goto(`${BASE}/login`, { waitUntil: 'networkidle' });
await page.fill('#email', 'demo@arcistocks.local');
await page.fill('#password', 'demo123456');
await page.click('#email-submit');
await page.waitForURL('**/dashboard', { timeout: 10000 });
const acc = await page.evaluate(() => fetch('/api/signals/accuracy').then(r => r.json()));
console.log('accuracy API:', JSON.stringify(acc));
await page.goto(`${BASE}/dashboard/signals`, { waitUntil: 'networkidle' });
await page.waitForTimeout(1500);
const cardVisible = await page.isVisible('text=Signal accuracy');
console.log('accuracy card visible:', cardVisible);
console.log('page errors:', errs.length ? errs.map(e=>e.split(';')[0]) : '(none)');
await browser.close();
